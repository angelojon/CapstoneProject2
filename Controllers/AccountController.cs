        // POST: /Account/Register
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterViewModel model)
        {
            if (ModelState.IsValid)
            {
                // Check if the email already exists in the database
                var existingUser = await UserManager.FindByEmailAsync(model.Email);
                if (existingUser != null)
                {
                    ModelState.AddModelError("Email", "Email already exists.");
                    return View(model);
                }

                var user = new ApplicationUser
                {
                    UserName = model.Email,
                    Email = model.Email,
                    FirstName = model.FirstName,
                    LastName = model.LastName,
                    Address = model.Address,
                    ContactNumber = model.ContactNumber
                };

                var result = await UserManager.CreateAsync(user, model.Password);
                if (result.Succeeded)
                {
                    await UserManager.AddToRoleAsync(user.Id, "Customer");

                    // Create an AuditLogs record
                    var auditLog = new AuditLogs
                    {
                        Email = user.Email,
                        FirstName = user.FirstName,
                        LastName = user.LastName,
                        ContactNumber = user.ContactNumber,
                        Address = user.Address,
                        RegisterDate = DateTime.Now // Set the RegisterDate to the current date and time
                                                    // Other audit properties you may want to include
                    };

                    // Save the AuditLogs record to your database
                    // Assuming you have a DbContext and DbSet for AuditLogs
                    db.AuditLogs.Add(auditLog);
                    db.SaveChanges();

                    // Send email confirmation
                    string code = await UserManager.GenerateEmailConfirmationTokenAsync(user.Id);
                    var callbackUrl = Url.Action("ConfirmEmail", "Account", new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);
                    await UserManager.SendEmailAsync(user.Id, "Confirm your account", "Please confirm your account by clicking <a href=\"" + callbackUrl + "\">here</a>");

                    return RedirectToAction("Message", "Home"); // Redirect to a page with a verification message
                }
                AddErrors(result);
            }

            // If we got this far, something failed, redisplay the form
            return View(model);
        }

