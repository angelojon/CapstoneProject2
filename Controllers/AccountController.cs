        //
        // POST: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            var user = await UserManager.FindByEmailAsync(model.Email);

            if (user == null || user.EmailConfirmed == false)
            {
                // User does not exist or email is not confirmed, display a generic error message.
                ModelState.AddModelError("", "Invalid login attempt. Please check your email and password or confirm your email address.");
                return View(model);
            }
            if (user.AccountStatus == "Disable")
            {
                // Account is disabled due to violations, display a specific error message.
                ModelState.AddModelError("", "Your account has been disabled due to violations.");
                return View(model);
            }
            if (user != null && await UserManager.IsLockedOutAsync(user.Id))
            {
                // User is locked out
                return View("Lockout");
            }

            var result = await SignInManager.PasswordSignInAsync(model.Email, model.Password, model.RememberMe, shouldLockout: true);

            if (result == SignInStatus.Success)
            {
                if (await UserManager.GetLockoutEnabledAsync(user.Id))
                {
                    await UserManager.ResetAccessFailedCountAsync(user.Id);
                }

                // Check if an entry with the same email exists in AuditLogs
                var auditLog = db.AuditLogs.FirstOrDefault(log => log.Email == user.Email);

                if (auditLog != null)
                {
                    // Update the existing AuditLogs entry's LoginDate
                    auditLog.LoginDate = DateTime.Now;
                    // Save changes to the database
                    db.SaveChanges();
                }
                else
                {
                    // Create a new AuditLogs entry
                    auditLog = new AuditLogs
                    {
                        Email = user.Email,
                        LoginDate = DateTime.Now,
                        // Other audit properties you may want to include
                    };

                    // Add the new AuditLogs entry to the context
                    db.AuditLogs.Add(auditLog);
                    // Save changes to the database
                    db.SaveChanges();
                }

                var roles = await UserManager.GetRolesAsync(user.Id);

                if (roles.Contains("Customer"))
                {
                    return RedirectToAction("Index", "Home");
                }
                else if (roles.Contains("Admin"))
                {
                    return RedirectToAction("AdminHome", "Home");
                }
                else if (roles.Contains("Owner")){
                    return RedirectToAction("AdminHome", "Home");
                }
                else
                {
                    return RedirectToLocal(returnUrl);
                }
            }
            else if (result == SignInStatus.LockedOut)
            {
                return View("Lockout");
            }
            else
            {
                // Handle other SignInStatus cases
                ModelState.AddModelError("", "Invalid login attempt. Please check your email and password.");
                return View(model);
            }
        }




        //
        // GET: /Account/VerifyCode
        [AllowAnonymous]
        public async Task<ActionResult> VerifyCode(string provider, string returnUrl, bool rememberMe)
        {
            // Require that the user has already logged in via username/password or external login
            if (!await SignInManager.HasBeenVerifiedAsync())
            {
                return View("Error");
            }
            return View(new VerifyCodeViewModel { Provider = provider, ReturnUrl = returnUrl, RememberMe = rememberMe });
        }

        //
        // POST: /Account/VerifyCode
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> VerifyCode(VerifyCodeViewModel model)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            // The following code protects for brute force attacks against the two factor codes. 
            // If a user enters incorrect codes for a specified amount of time then the user account 
            // will be locked out for a specified amount of time. 
            // You can configure the account lockout settings in IdentityConfig
            var result = await SignInManager.TwoFactorSignInAsync(model.Provider, model.Code, isPersistent: model.RememberMe, rememberBrowser: model.RememberBrowser);
            switch (result)
            {
                case SignInStatus.Success:
                    return RedirectToLocal(model.ReturnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Invalid code.");
                    return View(model);
            }
        }

